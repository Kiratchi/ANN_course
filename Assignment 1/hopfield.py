def store_patterns(patterns):
    p = patterns.shape[0]
    N = patterns.shape[1]*patterns.shape[2]
    x = patterns.reshape(p,N)
    w = (x.T @ x) / N
    np.fill_diagonal(w,0)
    return w

w = store_patterns(patterns)

def sgn(x):
    return 1 if x >= 0 else -1

def run_network(s1, w, max_steps = 1000):
    orig_shape = s1.shape
    s = np.ravel(s1).copy()
    S = [s.reshape(orig_shape).copy()]
    N = s.size
    ss_count = 0
    for t in range(max_steps):
        s_prev = s.copy()

        i = t % N
        b = w[i,:] @ s
        s[i] = sgn(b)


        if np.array_equal(s, s_prev):
            ss_count +=1
        else:
            ss_count = 0

        if ss_count == N:
            print(f"Updates needed: {t-1}")
            return(S)
        
        S.append(s.reshape(orig_shape).copy())

    print("Reached maximum iterations")
    return S


